int	ft_recursive_factorial(int nb)
{
	int	result;

	result = 0;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	result = nb * ft_recursive_factorial(nb - 1);
	return (result);
}

#include <stdio.h>
int main()
{
    int i = 10;
    printf("%d\n", ft_recursive_factorial(i));
}